/// <summary>
/// Represents the JSON data of a tile localization.
/// </summary>
public class TileLocalization
{
	/// <summary>
	/// The name of this tile.
	/// </summary>
	public string name;

	/// <summary>
	/// The path to the texture to use for this tile.
	/// </summary>
	public string file;

	/// <summary>
	/// The description of this tile's effects.
	/// </summary>
	public string desc;
}
